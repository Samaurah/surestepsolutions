						<h2 id="started">Implementation Methodology</h2>
						<p>The Microsoft Dynamics Sure Step Implementation methodology describes consulting and process best practices for partners working with Microsoft Dynamics NAV.</p>
						<p><img id="method" src="<?php echo base_url();?>Images/methodology.png"></p>
						<p>The smart guide to the successful delivery of Microsoft Dynamics Business Solutions</p>
						<p style="margin-left:15px"><i class="fa fa-check-circle"></i>      Learn how to effectively use Microsoft Dynamics to implement the right Dynamics business solution with quality, on-time and on-budget results.
						</p>
						<p style="margin-left:15px"><i class="fa fa-check-circle"></i>      Understand the review and optimization offerings available from Microsoft Dynamics to further enhance your business solution delivery during and after go-live.
						</p>
						<p>The Microsoft Dynamics solutions have been designed to be familiar to users, work easily with the existing systems that customers have already deployed, empower people and teams to be productive, and help organizations drive business success. The ERP suite provides functionality to help business in the areas of financial planning and accounting, supply chain, project accounting, field service, and human resources processes. On the other hand, the CRM solution allows companies to streamline the way their employees communicate and collaborate with their customers with features such as customer relationship, sales force automation, service management, marketing capabilities and workflow automation and analytics.</p>
						<p>Although business solutions are designed to achieve the same results within organizations, customers usually seek for very specific solutions for resolving their unique problems and supporting the business challenges as envisioned by them. No matter how rich the functionality of the solution is, unique customer's expectations cannot just be delivered off the shelf. This gap needs to be bridged by the implementation process. One might think that implementing business solutions in small and mid-sized companies is less complex compared to the large-scaled implementations in corporations. Be careful not to jump to conclusions here. In general, business processes are less standardized in these types of businesses. Instead, you will find a rich and interesting variety of procedures representing the unique way of doing things. This makes the need for a unique business solution even greater and demands a streamlined implementation process.</p>
						<h3>Diagnostic Phase</h3>
						<p>The Diagnostic phase starts during the sales process and ends with an accepted proposal and contract for the next part of the implementation project. The goal of the Diagnostic phase is to gather enough information in order to define the high level project scope and make a confident proposal for the remaining phases of the implementation. The major deliverable is the implementation proposal and contract. There will also be high level and possibly detailed requirements and gap/fit analysis documents.					</p>
						<h3>Analysis Phase</h3>
						<p>The Analysis phase represents the official start of the implementation project. The goal is to define and agree upon the business requirements related to the new business software solution. This includes the business and data migration requirements, as well as integration requirements. The major deliverable is the comprehensive functional requirements document.</p>
						<h3>Design Phase</h3>
						<p>The goal of the Design phase is to define how the business requirements will be implemented. The phase includes design of both the business software solution configuration and the design of specific customizations and integrations needed to satisfy business requirements identified during the Analysis phase. The customizations can range from simple user interface or report modifications to complex functionality additions or modifications. The phase also includes mapping and designing processes for data migration.</p>
						<h3>Development Phase</h3>
						<p>The goal of the Development phase is to develop the customizations, integrations and data migration processes that are defined and approved in the design specifications. The major deliverables are the completed feature customizations, reports, integrations and data migration processes. Each component is tested and verified to be functioning according to specifications.</p>
						<h3>Deployment Phase</h3>
						<p>The Deployment phase is where the efforts of the project team come together for a successful transition to the new business software solution. There are several important activities that must be completed in order to reach the end goal. The phase includes the activities related to final system and load testing, training End Users, and the actual cut-over to the new production environment.</p>
						<h3>Operation Phase</h3>
						<p>The goal of the Operation phase is to transition the customer from the implementation project into on-going support following a successful go-live. The deliverables for this phase include final project and software related documentation, project review documentation, and on-going support contracts. At the end of this phase, the project will be transitioned to the customer and on-going support.</p>
						<h3>Optimization Phase</h3>
						<p>The purpose of the Optimization phase is to review a customerâ€™s existing business software implementation and make adjustments to increase the effectiveness of the solution. The major deliverable for the Optimization phase is an optimized, better functioning production environment.</p>
						<h3>Upgrade Phase</h3>
						<p>The purpose of the Upgrade phase is to perform a basic upgrade of a customerâ€™s existing business software implementation to a new version of the software solution. The major deliverable for the Upgrade phase is an upgraded, better functioning production environment.</p>
